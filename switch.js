function caseswitch(val)
{
    switch(val)
    {
        case 1:
            console.log("1");
            break;
        case 2:
                console.log("2");
            break;
        case 3:
                console.log("3");
            break;
        case 4:
                console.log("4");
            break;
        default:
            console.log("hello0");
    }
}

caseswitch(4);